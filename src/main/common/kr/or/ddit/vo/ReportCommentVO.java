package kr.or.ddit.vo;

public class ReportCommentVO {
	private String report_no;
	private String mem_id;
	private String report_comment_regdate;
	private String report_comment_content;
	private String report_seq;
	public String getReport_no() {
		return report_no;
	}
	public void setReport_no(String report_no) {
		this.report_no = report_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getReport_comment_regdate() {
		return report_comment_regdate;
	}
	public void setReport_comment_regdate(String report_comment_regdate) {
		this.report_comment_regdate = report_comment_regdate;
	}
	public String getReport_comment_content() {
		return report_comment_content;
	}
	public void setReport_comment_content(String report_comment_content) {
		this.report_comment_content = report_comment_content;
	}
	public String getReport_seq() {
		return report_seq;
	}
	public void setReport_seq(String report_seq) {
		this.report_seq = report_seq;
	}
	
	
}
