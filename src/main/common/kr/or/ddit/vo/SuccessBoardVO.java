package kr.or.ddit.vo;

public class SuccessBoardVO {
	private String r;
	private String success_no;
	private String project_no;
	private String mem_id;
	private String success_title;
	private String success_content;
	private String success_regdate;
	private String success_hit;
	
	public String getR() {
		return r;
	}
	public void setR(String r) {
		this.r = r;
	}
	public String getSuccess_no() {
		return success_no;
	}
	public void setSuccess_no(String success_no) {
		this.success_no = success_no;
	}
	public String getProject_no() {
		return project_no;
	}
	public void setProject_no(String project_no) {
		this.project_no = project_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getSuccess_title() {
		return success_title;
	}
	public void setSuccess_title(String success_title) {
		this.success_title = success_title;
	}
	public String getSuccess_content() {
		return success_content;
	}
	public void setSuccess_content(String success_content) {
		this.success_content = success_content;
	}
	public String getSuccess_regdate() {
		return success_regdate;
	}
	public void setSuccess_regdate(String success_regdate) {
		this.success_regdate = success_regdate;
	}
	public String getSuccess_hit() {
		return success_hit;
	}
	public void setSuccess_hit(String success_hit) {
		this.success_hit = success_hit;
	}
}
